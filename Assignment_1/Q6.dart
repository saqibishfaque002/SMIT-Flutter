void main() {
  // Define a variable to store the alphabet
  String alphabet = 'a';
  // Check if the alphabet is a vowel
  if (alphabet == 'a' ||
      alphabet == 'e' ||
      alphabet == 'i' ||
      alphabet == 'o' ||
      alphabet == 'u') {
    // The alphabet is a vowel
    print('The alphabet $alphabet is a vowel');
  } else {
    // The alphabet is a consonant
    print('The alphabet $alphabet is a consonant');
  }
}
